namespace App\Http\Requests;

use App\Models\User;
use Illuminate\Contracts\Validation\ValidationRule;
use Illuminate\Foundation\Http\FormRequest;
use Illuminate\Validation\Rule;

class ProfileUpdateRequest extends FormRequest
{
    /**
     * Get the validation rules that apply to the request.
     *
     * @return array<string, ValidationRule|array<mixed>|string>
     */
    public function rules(): array
    {
        return [
            'name' => ['required', 'string', 'max:255'],
            'email' => [
                'required',
                'string',
                'lowercase',
                'email',
                'max:255',
                Rule::unique(User::class)->ignore($this->user()->id),
                // Block claiming an address that's already pending
                // verification by another account — defense in depth
                // on top of the verify-time race re-check.
                Rule::unique('email_change_requests', 'new_email')
                    ->where(fn ($q) => $q->where('user_id', '!=', $this->user()->id)),
            ],
        ];
    }
}
